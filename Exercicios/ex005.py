#Desafio 005 - Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.
num_int = int(input('Digite um número inteiro:'))
antecessor = num_int - 1
sucessor = num_int + 1
print(f'Você digitou o número {num_int} e o seu antecessor é o número {antecessor} e seu sucessor é o número {sucessor}.')

